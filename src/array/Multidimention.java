package array;

public class Multidimention {
	
	public static void main(String[]args){
		//deaclare and initialize
		String name[][]= {{"A","H","I"},
				          {"D","B","E"},
				          {"1","F","C","G"}};
		//use or print
		{
			System.out.println(name[0][0]+name[2][0]);
			System.out.println(name[1][1]);
			System.out.println(name[2][2]);
			System.out.println(name[1][0]);
			System.out.println(name[1][2]);
			System.out.println(name[2][1]);
			System.out.println(name[2][3]);
			System.out.println(name[0][1]);
			System.out.println(name[0][2]);
			
			
		}
		
	}

}
