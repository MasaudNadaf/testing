package loop;

public class Do {
	
	
	public static void main(String[] args) {
		int a=10;
		//while loop
				while(a<=15)
				{
					System.out.println(a);
					a++;
				}
				System.out.println("end of while loop");
		
		
		//do while loop	
		do
		{
			System.out.println(a);
			a--;		
		}
		while (a>=9);
		  System.out.println("end of do loop");
	}
}		

		
